# Calls the venv's uvicorn directly, not `uv run uvicorn` — `uv run`'s
# ~/.cache/uv write on startup conflicts with ProtectHome=read-only below.
_DINARY_UVICORN = "/home/ubuntu/dinary/.venv/bin/uvicorn"
_DINARY_EXEC = (
    "ExecStart=/bin/sh -c 'exec " + _DINARY_UVICORN + " dinary.main:app --host {host} --port 8000'"
)

# network.target doesn't wait for tailscaled (seen in practice after a reboot),
# so this blocks startup until it's ready.
_DINARY_EXEC_START_PRE_TAILSCALE = (
    "ExecStartPre=/bin/sh -c 'for i in $(seq 1 30); do "
    "tailscale ip -4 >/dev/null 2>&1 && exit 0; sleep 1; done; exit 1'"
)

# Confines the app to the minimum authority it needs so a code-exec bug in a
# dependency can't read secrets outside ~/dinary or touch system state.
_DINARY_SANDBOX = (
    "NoNewPrivileges=true\n"
    "ProtectSystem=strict\n"
    "ProtectHome=read-only\n"
    "ReadWritePaths=/home/ubuntu/dinary/data\n"
    "PrivateTmp=true\n"
    "ProtectKernelTunables=true\n"
    "ProtectKernelModules=true\n"
    "ProtectKernelLogs=true\n"
    "ProtectControlGroups=true\n"
    "RestrictSUIDSGID=true\n"
    "RestrictRealtime=true\n"
    "RestrictNamespaces=true\n"
    "LockPersonality=true\n"
    "MemoryDenyWriteExecute=true\n"
    "SystemCallArchitectures=native\n"
    "CapabilityBoundingSet=\n"
    "AmbientCapabilities=\n"
)

